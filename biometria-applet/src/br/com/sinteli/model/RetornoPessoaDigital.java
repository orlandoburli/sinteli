package br.com.sinteli.model;

public class RetornoPessoaDigital {

	private String PesDigitalCod;
	private String PesCod;
	private String PesDigitalString;
	
	public String getPesDigitalCod() {
		return PesDigitalCod;
	}
	public void setPesDigitalCod(String pesDigitalCod) {
		this.PesDigitalCod = pesDigitalCod;
	}
	public String getPesCod() {
		return PesCod;
	}
	public void setPesCod(String pesCod) {
		this.PesCod = pesCod;
	}
	public String getPesDigitalString() {
		return PesDigitalString;
	}
	public void setPesDigitalString(String pesDigitalString) {
		this.PesDigitalString = pesDigitalString;
	}
}
